# encontrado el número mayor de una lista
numeros = [4, 7, 1, 42, 15]

numero_alto = max(numeros)
print(numero_alto)

# encontrado el número más bajo de una lista
print(min(numeros))

# Redondeando a 2 decimales
numero = round(12.345678, 2) # Redondeando a 2 decimales (Sin otro argumento, redondea a 0 decimales)
print(numero)

# Retorna true, si todos los valores son verdaderos
resultado_all = all([0, "true", [123, 1212]])
print(resultado_all)

# Suamr los valores de un iterable
resultado_sum = sum([1, 2, 3, 4, 5])
print(resultado_sum)