# Pedir un número al usuario
numero = input("Dame un número: ") # El número se guarda como string

# Convertir el número a entero y lo múltiplico por 2
resultado = (int(numero) * 2)

# Convertir el número a flotante y lo múltiplico por 2
resultado2 = (float(numero) * 2)

# IMPORTANTE: Si trabajamos con números decimales trabajar con float y sino con int porque al ejecutar dar error

print(resultado)
print(resultado2)