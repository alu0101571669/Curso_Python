# Suma y resta
suma = 10 + 5
resta = 12 - 5

# Multiplicación y división
multi = 10 * 5
div = 10 / 2 # La división nos devuelve un número flotante

# Potencia
exponente = 10**2

# División Baja (//)
division_baja = 10 // 3 # Nos devuelve un número entero redondeado hacia abajo

# Módulo
modulo = 10 % 3

# Como saber el tipo de dato de una variable
print(type([1, 2, 3]))

# Mostrar los resultados
print(suma)
print(resta)
print(multi)
print(div)
print(exponente)
print(division_baja)
